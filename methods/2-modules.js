// Defining two constants 'name1' and 'name2' with string values.
const name1 = "ajmal";
const name2 = "anand";

// Defining a function 'sayHi' that logs a greeting message with the provided name.
const sayHi = (name) => {
  console.log(`Hello There ${name}`);
};

// Invoking 'sayHi' function with the string "yaseen".
sayHi("yaseen");

// Invoking 'sayHi' function with the value of constant 'name1'.
sayHi(name1);

// Invoking 'sayHi' function with the value of constant 'name2'.
sayHi(name2);
